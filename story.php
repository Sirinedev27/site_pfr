<p> Henri Vicariot est le principal architecte de l’aérogare d'Orly Sud. Il s'associe notamment à Jean Prouvé à qui il laisse l'aménagement intérieur du bâtiment.

Charpente d'acier et de béton, murs rideaux (en verre) développés par Saint-Gobain, sonorisation de l'aéroport inédite avec 3000 haut-parleurs, lumière omniprésente dans toute l'aérogare, terrasse, etc. 

Orly Sud devient à partir de 1961 une véritable attraction touristique où l'on vient en famille le dimanche se balader et regarder les avions décoller. </p> 

<p> Si jamais un ouvrage justifia la fierté de ceux qui l’ont édifié de leurs cerveaux et de leurs mains, c’est bien celui que voilà, à la rencontre du ciel et de la terre.
Le président Charles de Gaulle
Extrait de son discours lors de l'inauguration d'Orly Sud en 1961 </p> 

