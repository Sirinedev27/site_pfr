<p> Interview de Paul Damm, conservateur du patrimoine et auteur (avec Laurence Bartoletti) de Orly, Aéroport des Sixties, livre paru en 2020 aux éditions Lieux-Dits, en partenariat avec le Groupe ADP et le musée Air France. </p>

<p> Avec la construction de l'aéroport Paris-Orly, c'est enfin tout le réseau urbain du sud parisien qui se dessine autour et vers l'aéroport.

De nos jours, l'aéroport de Paris-Orly possède quatre sections : Orly 1, Orly 2, Orly 3 et Orly 4. C'est le deuxième aéroport parisien après Paris-Charles de Gaulle. Il est relié directement par le RER B et l'OrlyVal.

En 2019, près de 32 000 de passagers sont passés par cet aéroport mythique. </p>


