<h2> L'aéroport de Paris-Orly fête en 2021 ses 60 ans </h2> 

<p> Le 24 février 1961, le président Charles de Gaulle inaugure en grande pompe l'aérogare d'Orly Sud. 
Première grande réalisation de l'Aéroport de Paris, qui va y installer les métiers phares de l'entreprise 
- conception et exploitation aéroportuaires - le nouvel aéroport fait entrer définitivement Paris dans la modernité et positionne à nouveau la France comme une grande puissance mondiale. 
Retour sur 60 ans d'Histoire ! </p> 

<p> A peine 16 ans après la Seconde Guerre mondiale, Orly est le symbole tangible d'un monde en plein changement. En effet, comme toutes les grandes infrastructures de transport, le nouvel grand aéroport de l'Ouest de Paris s'adapte aux flux et aux nouvelles technologies. Véritable prouesse architecturale, technique et esthétique, il représente non seulement le présent, mais aussi le futur d'une France en mouvement. </p>

