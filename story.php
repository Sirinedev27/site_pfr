<p> Du choix du mobilier, à la galerie marchande et la zone de transit, en passant par les restaurants, le cinéma et l'hôtel, ainsi que les tenues de grands couturiers du personnel d'accueil, rien n'est oublié pour faire vivre aux passagers un moment unique et privilégié. Une manifestation du chic parisien et du luxe international.

Enfin, l'aéroport d'Orly est tellement populaire qu'il devient dans les années 1960 le décor de nombreux films et le passage obligé de nombreuses personnalités. </p> 

